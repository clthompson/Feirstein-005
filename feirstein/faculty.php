					<h5>Jonathan Wacks, Directing</h5>
					<p>Jonathan Wacks is the founding director of the Barry R. Feirstein Graduate School of Cinema at Steiner Studios and professor of film at Brooklyn College. He has directed a number of films, including <em>Powwow Highway </em>(Warner Bros.)<em>,</em> produced by George Harrison. The film was winner of the Sundance Film Festival Filmmakers Trophy and of awards for best picture, director and actor at the American Indian Film Festival in San Francisco, and was nominated for four Independent Spirit Awards. Wacks&rsquo; first film, <em>Crossroads/South Africa </em>(PBS), won a Student Academy Award in the documentary category. He then produced the acclaimed cult-hit <em>Repo Man </em>(Universal)<em>,</em> starring Emilio Estevez and Harry Dean Stanton, and directed <em>Mystery Date </em>(Orion), starring Ethan Hawke and Teri Polo, and <em>Ed and His Dead Mother,</em> starring Steve Buscemi and Ned Beatty. He also directed an array of television productions, including <em>21 Jump Street</em> (Johnny Depp), <em>Sirens</em> and <em>Going to Extremes</em>.</p>
 					<p>Prior to his career as a director, Wacks served as vice president of production at the Samuel Goldwyn Company. He is a former chairman of the board of the Independent Feature Project/West (now Film Independent), the largest organization of independent filmmakers in the United States, and has served on the selection committee of the Writers&rsquo; Program at the Sundance Institute. His work has been seen at numerous international film festivals, including Sundance, Montreal, Tokyo, Florence, London, Leipzig, Leeds, Cape Town, Deauville, New York, Munich and Berlin.</p>
 					<p>Wacks has written several screenplays, including <em>Recoil, </em>based on the Jim Thompson novel, <em>No Cure for Love</em>, <em>My African Heart, Coldsleep</em> <em>Lullaby</em> and <em>Stuck. </em>He served as chair of the Visual and Media Arts Department at Emerson College, head of the Film Department at the Vancouver Film School in British Columbia and chair of the Moving Image Arts Department at the College of Santa Fe. He was also director of Garson Studios in Santa Fe, N.M. Wacks holds a B.A. (with honours) from the University of Essex (United Kingdom) and an M.F.A. from the UCLA School of Theater, Film and Television. He is a member of the Directors Guild of America.</p>
