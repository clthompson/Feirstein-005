							<div class="faq-category">
								<h5>Application</h5><span>&#9662;</span>
							</div>
							<div class="faq-questions">
								<div class="question">
									<p>
										<strong>Can I apply to more than one master’s program?</strong>
									</p>
									<p class="answer">
										No. Applicants may apply to only one master’s program. However, applicants to the M.F.A. in Cinema Arts Program may select two specializations on the application form, indicating their first choice.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>How much is the application fee?</strong>
									</p>
									<p class="answer">
										The application fee is $125. There is an additional $6 fee for M.F.A. applicants using Slideroom to upload a portfolio.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>Will scholarships be available?</strong>
									</p>
									<p class="answer">
										Scholarships will be available based on need. For more information, please e-mail the <a href="mailto:feirstein@brooklyn.cuny.edu?subject=Scholarships Inquiry">Office of Graduate Admissions</a>.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>Do I have to submit official transcripts?</strong>
									</p>
									<p class="answer">
										Yes. Applicants must mail official transcripts from all colleges and universities attended to the Office of Graduate Admissions.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>I graduated from a college outside the United States. Do I need to have my transcript evaluated?</strong>
									</p>
									<p class="answer">
										Yes, you need to obtain an international credential evaluation from a professional agency that is a member of <a href="http://www.naces.org"  target="_blank">NACES</a>. However, exceptions are made for <a href="http://www.brooklyn.cuny.edu/web/admissions/international/apply/graduate.php" target="_blank">certain countries</a>.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>Can letters of recommendation be submitted electronically?</strong>
									</p>
									<p class="answer">
										Yes. When you apply, register your recommenders in our online application system. Once you submit your application, they will be able to upload their letters electronically to us.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>How will I know my application status?</strong>
									</p>
									<p class="answer">
										Soon after you apply you will receive an e-mail from the Office of Graduate Admissions. An e-mail will be sent to you whenever there is a change in your status. You may check the status of your application by contacting the Office of Graduate Admissions at 718.951.4536 or by <a href="mailto:feirstein@brooklyn.cuny.edu?subject=Application Status">e-mail</a>.
									</p>
								</div>
							</div>
						</div>
						<div class="faq-item">
							<div class="faq-category">
								<h5>General</h5><span>&#9662;</span>
							</div>
							<div class="faq-questions">
								<div class="question">
									<p>
										<strong>What is the Feirstein Graduate School of Cinema?</strong>
									</p>
									<p class="answer">
										The Barry R. Feirstein Graduate School of Cinema at Steiner Studios is the first public graduate school of cinema in New York City, and the only school in the country situated on a working film lot. The school was formed with the objective of creating a world-class film school and providing affordable access to career opportunities in cinema. It is the result of extensive public-private partnerships.    
									</p>
								</div>
								<div class="question">
									<p>
										<strong>Is it still part of Brooklyn College?</strong>
									</p>
									<p class="answer">
										Yes, the Feirstein School is a graduate division of Brooklyn College. All questions regarding admission to Feirstein can be directed to our Office of Graduate Admissions via phone 718.951.4536 or <a href="mailto:feirstein@brooklyn.cuny.edu">e-mail us</a>.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>Where is the school located?</strong>
									</p>
									<p class="answer">
										The Feirstein Graduate School of Cinema will be located at Steiner Studios at the Brooklyn Navy Yard.
										The Brooklyn Navy Yard has become an area of private manufacturing and commercial activity, and is situated between the neighborhoods of Williamsburg, DUMBO, Fort Greene and Clinton Hill.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>Can I visit the school?</strong>
									</p>
									<p class="answer">
										We will be offering tours of the facility in the upcoming months; the school is currently <span class="boldedcopy">under construction</span> and off limits to visitors. If you would like to sign up for a future tour, please <a href="mailto:feirstein@brooklyn.cuny.edu?subject=Tour Request">e-mail us</a> and include "Tour Request" in the subject line.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>Will classes be offered at the Brooklyn College campus in Flatbush?</strong>
									</p>
									<p class="answer">
										No. All classes offered at the Feirstein Graduate School of Cinema will be held at the school’s facility at Steiner Studios.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>Will the school offer career advisement and counseling?</strong>
									</p>
									<p class="answer">
										Yes. As part of Brooklyn College, Feirstein students have full access to all of our resources, including the <a href="http://www.brooklyn.cuny.edu/web/academics/centers/magner.php"  target="_blank">Magner Career Center</a>, which offers career counseling, access to internships and job opportunities.
									</p>
								</div>
								<div class="question">
									<p>
										<strong>Is there on-campus housing?</strong>
									</p>
									<p class="answer">
										There is no housing at the Feirstein School’s location at Steiner Studios. Brooklyn College refers students to the privately owned <a href="http://www.brooklyn.cuny.edu/web/kenilworth.php" target="_blank">Residence Hall</a> near the college’s main campus in the Midwood neighborhood of Brooklyn.
									</p>
								</div>
							</div>
						</div>
